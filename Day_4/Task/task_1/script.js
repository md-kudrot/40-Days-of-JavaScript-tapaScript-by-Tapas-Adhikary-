let day = "Monday";

switch (day) {
   case "monday":
       console.log("It's the start of the week.");
       break;
   default:
       console.log("It's a normal day.");
}

// Output: It's a normal day.

/*
The value of the "day" variable is "Monday", and the condition in the case is "monday". 
the case condition does not match, 
so the default block executes and the output is "It's a normal day."
*/
